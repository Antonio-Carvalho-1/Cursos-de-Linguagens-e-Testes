import math
a = float(input('Digite um número coisa: '))


# math.sin() // função que retorna o seno
# math.cos() // função que retorna o cosseno
# math.tan() // função que retorna a tangente



print('Seno de {} é igual a {:.2f}\n'.format(a,math.sin(a)))
print('Cosseno de {} é igual a {:.2f}\n'.format(a,math.cos(a)))
print('tangente de {} é igual a {:.2f}\n'.format(a, math.tan(a)))
